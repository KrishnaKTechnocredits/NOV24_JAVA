package imran.Pattern_Program;

public class Assignment41_Pattern4Digit {

	void getPattern()
	{
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=4;j++)
			{
				System.out.print(i);
			}
			System.out.println("");
		}
	}
	
	public static void main(String[] args) {
		Assignment41_Pattern4Digit assignment41_Pattern4Digit_1 = new Assignment41_Pattern4Digit();
		assignment41_Pattern4Digit_1.getPattern();
	}
}
