package roshan.nestedloop.patternPrint;

public class PrintPatternAss4 {
	static void printPattern () {
		for(int i=1 ;i<=5;i++) {
			for(int j=1;j<=5;j++ ) {
				System.out.print(i + " " );
			}
			System.out.println();
		}
		
	}
	public static void main(String[] args) {
		PrintPatternAss4.printPattern();

}
}























